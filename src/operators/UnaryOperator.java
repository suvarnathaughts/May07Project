package operators;

public class UnaryOperator 
{
	public static void main(String args[])
	{
		int a =50;
		System.out.println(a);  //50
		
		a++;
		System.out.println(a);  //51
		
		++a;
		System.out.println(a);  //52
		
		a--;
		System.out.println(a);  //51
		
		--a;
		System.out.println(a);  //50
		
		
	}

}
