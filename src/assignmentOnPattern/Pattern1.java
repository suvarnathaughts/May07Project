package assignmentOnPattern;

public class Pattern1 {/*
	*
	**
	***
	****
	*****         */
	
	public static void main(String args[])
	{
		// no.of rows(i) = 5, no.of column (j)
		int n=5;
		for (int i=1;i<=n;i++)     // this for loop is for rows
		{
			for(int j=1;j<=i;j++)  // this for loop is for column
			{
				System.out.print("*");
			}
			System.out.println();
		} 
	}
	
	
	
	
	
	
	
	
	
	
	
	
	
	


}
