package assignmentOnDataTypes;

public class Example1 
{
	
	public static void main(String args []) 
	
	{
		// This are the two ways to call static method into main method
		
		
		Example1.booleanDatatype();
		
		booleanDatatype();
	}
	
	
	public static void booleanDatatype() 
	
	{
		// for boolean data type size= 1bit, accepts value like true or false
		
		boolean a = true;
		
		boolean b = false;
		
		System.out.println(a);
		
		System.out.println(b);
		
		
} 






}


