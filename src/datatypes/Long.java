package datatypes;

public class Long {
	
	public static void main(String args[])
	
	// here l or L use at the end for long data type, range of long data type is -2^63 to 2^63-1
	
	{
		long a = 9421412495l;
		
		long b = 7741026459L;
		
		System.out.println(a);
		
		System.out.println(b);
		
	}
	

}
