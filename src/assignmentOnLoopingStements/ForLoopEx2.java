package assignmentOnLoopingStements;

public class ForLoopEx2 {
	
	// WAP to display alphabet from  A to Z  or  a to z 
	
	public static void main(String args[])
	{
		for(char ch= 'A'; ch<='Z';ch++)
		{
			System.out.println(ch);
		}
		
		for(char ch = 'a';ch<='z'; ch++)
		{
			System.out.println(ch);
		}
	}

}
